    document.addEventListener('DOMContentLoaded', function() {
        const form = document.getElementById('hireUsForm');
        const fileUpload = document.getElementById('file-upload');
        const fileList = document.getElementById('file-list');
        const successMessage = document.getElementById('success-message');

        // File upload handling
        fileUpload.addEventListener('change', function() {
            fileList.innerHTML = '';

            if (this.files.length > 0) {
                const filesHeader = document.createElement('p');
                filesHeader.textContent = 'Selected files:';
                fileList.appendChild(filesHeader);

                const filesList = document.createElement('ul');
                filesList.className = 'list-disc pl-5 mt-1';

                let totalSize = 0;
                let invalidFiles = [];

                // Check each file
                for (let i = 0; i < this.files.length; i++) {
                    const file = this.files[i];
                    totalSize += file.size;

                    // Check file type and size
                    const validTypes = ['.pdf', '.doc', '.docx', '.png', '.jpg', '.jpeg'];
                    const extension = '.' + file.name.split('.').pop().toLowerCase();

                    if (!validTypes.includes(extension) || file.size > 10485760) {
                        invalidFiles.push(file.name);
                    }

                    const listItem = document.createElement('li');
                    listItem.textContent = file.name + ' (' + formatFileSize(file.size) + ')';
                    filesList.appendChild(listItem);
                }

                fileList.appendChild(filesList);

                // Show error message if files are invalid
                if (invalidFiles.length > 0 || totalSize > 10485760) {
                    const errorMsg = document.createElement('p');
                    errorMsg.className = 'text-red-600 mt-2';
                    errorMsg.textContent = totalSize > 10485760 ?
                        'Total file size exceeds 10MB limit.' :
                        'Some files have invalid format. Please upload only PDF, DOC, or PNG files.';
                    fileList.appendChild(errorMsg);
                }
            }
        });

        // Helper functions
        function formatFileSize(bytes) {
            if (bytes < 1024) return bytes + ' bytes';
            else if (bytes < 1048576) return (bytes / 1024).toFixed(1) + ' KB';
            else return (bytes / 1048576).toFixed(1) + ' MB';
        }

        function showError(field, message) {
            const errorElement = field.parentElement.querySelector('.error-message') ||
